##operações com números

# +
# -
# *
# /
# % -> resto
# ** -> exponencial

#+

valor1 = 10
valor2 = 15
soma = valor1 + valor2
print(soma)



#-
valor1 = 15
valor2 = 10
subtraçao = valor1 - valor2
print(subtraçao)



#*
valor1 = 20
valor2 = 20
multiplicaçao = valor1 * valor2
print(multiplicaçao)



#/
valor1 = 10
valor2 = 2
divisao = valor1 / valor2
print(divisao)



#%
valor1 = 100
valor2 = 50
resto = valor1 % valor2
print(resto)



#**
valor1 = 8
exponencial = valor1 **5
print(exponencial)
